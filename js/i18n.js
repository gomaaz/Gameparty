// ---- Public API ----
let _lang = localStorage.getItem('gameparty_lang') || 'en';

function t(key, ...args) {
    const dict = TRANSLATIONS[_lang] || TRANSLATIONS.en;
    const s = dict[key] !== undefined ? dict[key] : (TRANSLATIONS.en[key] !== undefined ? TRANSLATIONS.en[key] : key);
    return s.replace(/\{(\d+)\}/g, (_, i) => args[i] !== undefined ? args[i] : '');
}

function getLang() { return _lang; }

function setLang(lang) {
    if (!TRANSLATIONS[lang]) return;
    _lang = lang;
    localStorage.setItem('gameparty_lang', lang);
    // Re-render active view (function defined in app.js)
    if (typeof refreshActiveView === 'function') refreshActiveView();
    // Update nav labels
    updateNavLabels();
    // Update lang button
    updateLangBtn();
}

function updateNavLabels() {
    const map = {
        dashboard: t('nav_home'),
        matcher: t('nav_games'),
        session: t('nav_session'),
        shop: t('nav_shop'),
        challenges: t('nav_duels'),
        activities: t('nav_activities'),
        profile: t('nav_profile'),
    };
    document.querySelectorAll('.nav-item[data-view]').forEach(btn => {
        const span = btn.querySelector('span:last-child');
        if (span && map[btn.dataset.view]) span.textContent = map[btn.dataset.view];
    });
}

function updateLangBtn() {
    const btn = document.getElementById('lang-toggle-btn');
    if (btn) btn.textContent = _lang === 'en' ? '🇬🇧' : '🇩🇪';
}
